    public ArrayList<Chambrecontenu> contenu(){
        ArrayList<Chambrecontenu> con=null ;
     con=new ArrayList<Chambrecontenu>();
     try{
     String sql="SELECT * FROM `chambres`;";
     st=conn.createStatement();
     rs=st.executeQuery(sql);
     Chambrecontenu chambre;
     while(rs.next()){
       chambre = new Chambrecontenu(rs.getInt(1),rs.getInt(2),rs.getString("etage"),rs.getInt(4));
     con.add(chambre);
     }
     }catch(Exception ex){
     Logger.getLogger(Reservation.class.getName()).log(Level.SEVERE, null, ex);
     }
        
        return con ;
    }
    public void remplissage(){
     ArrayList<Chambrecontenu> con= contenu();
     DefaultTableModel mod=(DefaultTableModel)table.getModel();
     mod.setRowCount(0);
     Object[] rw=new Object[4];
     for(int i = 0; i < con.size(); i++){
         rw[0]=con.get(i).getChambre_id();
         rw[1]=con.get(i).getNumero();
         rw[2]=con.get(i).getEtage();
         rw[3]=con.get(i).getNb_place();
         mod.addRow(rw);
     }
 }
    

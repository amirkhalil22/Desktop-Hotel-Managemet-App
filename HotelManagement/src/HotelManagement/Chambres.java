    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String etage = txtetage.getText();
        if(etage.trim().equals("")
                ){
            JOptionPane.showMessageDialog(null, "Remplir tous les champs");
        } else if(txtchambreid.getText()!=null || txtetage.getText()!=null || 
                 txtnum!=null || txtplace!=null ) {
            
            try {
                String sql="SELECT chambre_id FROM `chambres` where chambre_id =?;";
                set=conn.prepareStatement(sql);
                set.setInt(1, Integer.parseInt(txtchambreid.getText()));
                rs= set.executeQuery();
                if(rs.next()){
                        JOptionPane.showMessageDialog(null, "Chambre ID déja existe");
                    }else{
                      try{
                          String sql1="SELECT numero FROM `chambres` where numero =?;";
                set=conn.prepareStatement(sql1);
                set.setInt(1, Integer.parseInt(txtnum.getText()));
                rs= set.executeQuery();
                if(rs.next()){
                        JOptionPane.showMessageDialog(null, "Numero Chambre déja existe");
                    }else{
                    
                    
                    try{
                    String sql2 = "INSERT INTO `chambres`(chambre_id, numero, etage, nb_place) VALUES (?, ?, ?, ?);";
                    set=conn.prepareStatement(sql2);
                    set.setInt(1, Integer.parseInt(txtchambreid.getText()));
                    set.setInt(2, Integer.parseInt(txtnum.getText()));
                    set.setString(3, etage);
                    set.setInt(4, Integer.parseInt(txtplace.getText()));
                    int res=set.executeUpdate();
                    remplissage();
                    if(res<=1){
                    JOptionPane.showMessageDialog(null, "Succéess" );
                }else{
                    JOptionPane.showMessageDialog(null, "Erreur");
                }
                    } catch (SQLException ex) {
                Logger.getLogger(Chambres.class.getName()).log(Level.SEVERE, null, ex);
            }
                }
                     }catch(Exception ex){
                             Logger.getLogger(Chambres.class.getName()).log(Level.SEVERE, null, ex);
                         }
                }
            } catch (SQLException ex) {
                Logger.getLogger(Chambres.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_jButton4ActionPerformed

